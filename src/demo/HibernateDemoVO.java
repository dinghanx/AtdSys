package demo;

public class HibernateDemoVO {
	
	private Integer mem_id; 
	private Integer friend_id; 
	private Integer status;
	
	public final Integer getMem_id() {
		return mem_id;
	}
	public final void setMem_id(Integer mem_id) {
		this.mem_id = mem_id;
	}
	public final Integer getFriend_id() {
		return friend_id;
	}
	public final void setFriend_id(Integer friend_id) {
		this.friend_id = friend_id;
	}
	public final Integer getStatus() {
		return status;
	}
	public final void setStatus(Integer status) {
		this.status = status;
	} 
	
}
